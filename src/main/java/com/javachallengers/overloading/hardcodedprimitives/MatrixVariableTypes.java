package com.javachallengers.overloading.hardcodedprimitives;

public class MatrixVariableTypes {

    public static void main(String... hardcodedPrimitiveTypes) {
        System.out.println(1); // It's an int
        System.out.println(1.0); // It's a double
        System.out.println(1F); // It's a float
        System.out.println(1f); // It's a float
        System.out.println(1D); // It's a double
        System.out.println(1d); // It's a double
        System.out.println('1'); // It's a char
        System.out.println(true); // It's a boolean
    }

}
